package kr.ac.cultural.model;

public class Korea {
private int ntnum;
private String ntname;
private String ntregion;
private String ntimg;
public int getNtnum() {
	return ntnum;
}
public void setNtnum(int ntnum) {
	this.ntnum = ntnum;
}
public String getNtname() {
	return ntname;
}
public void setNtname(String ntname) {
	this.ntname = ntname;
}
public String getNtregion() {
	return ntregion;
}
public void setNtregion(String ntregion) {
	this.ntregion = ntregion;
}
public String getNtimg() {
	return ntimg;
}
public void setNtimg(String ntimg) {
	this.ntimg = ntimg;
}
}
